package web.com.springweb.vo;

public class CategoryCount {
	
	  private String category;
	    private int cnt;

	    // Getter/Setter
	    public String getCategory() {
	        return category;
	    }
	    public void setCategory(String category) {
	        this.category = category;
	    }
	    public int getCnt() {
	        return cnt;
	    }
	    public void setCnt(int cnt) {
	        this.cnt = cnt;
	    }

}
